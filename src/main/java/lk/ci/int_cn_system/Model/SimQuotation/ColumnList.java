package lk.ci.int_cn_system.Model.SimQuotation;

public class ColumnList {
	
	private String id;
	
	private String perilName;
	
	private String alias;
	
	private String perilType;
	
	private String perilValue;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPerilName() {
		return perilName;
	}

	public void setPerilName(String perilName) {
		this.perilName = perilName;
	}

	public String getAlias() {
		return alias;
	}

	public void setAlias(String alias) {
		this.alias = alias;
	}

	public String getPerilType() {
		return perilType;
	}

	public void setPerilType(String perilType) {
		this.perilType = perilType;
	}

	public String getPerilValue() {
		return perilValue;
	}

	public void setPerilValue(String perilValue) {
		this.perilValue = perilValue;
	}

}
