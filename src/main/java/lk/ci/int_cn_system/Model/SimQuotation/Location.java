package lk.ci.int_cn_system.Model.SimQuotation;

public class Location {

	private String id;
	
	private String companyId;
	
	private String branchTypeId;
	
	private String initialName;
	
	private String code;
	
	private String isLocalEntity;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCompanyId() {
		return companyId;
	}

	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}

	public String getBranchTypeId() {
		return branchTypeId;
	}

	public void setBranchTypeId(String branchTypeId) {
		this.branchTypeId = branchTypeId;
	}

	public String getInitialName() {
		return initialName;
	}

	public void setInitialName(String initialName) {
		this.initialName = initialName;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getIsLocalEntity() {
		return isLocalEntity;
	}

	public void setIsLocalEntity(String isLocalEntity) {
		this.isLocalEntity = isLocalEntity;
	}


	
	
	
}
