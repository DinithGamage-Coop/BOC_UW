package lk.ci.int_cn_system.Model.MotorPolicy;

public class VehicleDetailsModel {

	String vehIdentificationNo;
	
	String sumInsured;
	
	String vehicleType;
	
	String registeredAs;
	
	String dateOfRegistered;
	
	String engineCapacity;
	
	String fuelType;
	
	String noOfSeats;
	
	String color;
	
	String yearOfManufacture;

	
	
	public String getVehIdentificationNo() {
		return vehIdentificationNo;
	}

	public void setVehIdentificationNo(String vehIdentificationNo) {
		this.vehIdentificationNo = vehIdentificationNo;
	}

	public String getSumInsured() {
		return sumInsured;
	}

	public void setSumInsured(String sumInsured) {
		this.sumInsured = sumInsured;
	}

	public String getVehicleType() {
		return vehicleType;
	}

	public void setVehicleType(String vehicleType) {
		this.vehicleType = vehicleType;
	}

	public String getRegisteredAs() {
		return registeredAs;
	}

	public void setRegisteredAs(String registeredAs) {
		this.registeredAs = registeredAs;
	}

	public String getDateOfRegistered() {
		return dateOfRegistered;
	}

	public void setDateOfRegistered(String dateOfRegistered) {
		this.dateOfRegistered = dateOfRegistered;
	}

	public String getEngineCapacity() {
		return engineCapacity;
	}

	public void setEngineCapacity(String engineCapacity) {
		this.engineCapacity = engineCapacity;
	}

	public String getFuelType() {
		return fuelType;
	}

	public void setFuelType(String fuelType) {
		this.fuelType = fuelType;
	}

	public String getNoOfSeats() {
		return noOfSeats;
	}

	public void setNoOfSeats(String noOfSeats) {
		this.noOfSeats = noOfSeats;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getYearOfManufacture() {
		return yearOfManufacture;
	}

	public void setYearOfManufacture(String yearOfManufacture) {
		this.yearOfManufacture = yearOfManufacture;
	}
	
}
